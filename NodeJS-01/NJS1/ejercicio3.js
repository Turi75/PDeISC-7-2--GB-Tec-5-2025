function sumar(a, b) { //se crea la funcion para sumar
    return a + b;
  }
  
  function restar(a, b) { //se crea la funcion para restar
    return a - b;
  }
  
  function multiplicar(a, b) { //se crea la funcion para multiplicar
    return a * b;
  }
  
  function dividir(a, b) { //se crea la funcion para dividir
    return a / b;
  }
  //muestra en la consola todos los calculos anteriormente 
  console.log(sumar(4, 5));
  console.log(restar(3, 6));
  console.log(multiplicar(2, 7));
  console.log(dividir(20, 4));